package J05076;

public class MatHang {
    private String ma, ten, loai;
    public MatHang(){}
    public MatHang(String ma, String ten, String loai) {
        this.ma = ma;
        this.ten = ten;
        this.loai = loai;
    }
    public String getMa() {
        return ma;
    }
    public String getTen() {
        return ten;
    }
    public String getLoai() {
        return loai;
    }
}
